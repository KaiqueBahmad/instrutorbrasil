package kaiquebt.dev.instrutorbrasil.repository;

import kaiquebt.dev.instrutorbrasil.model.EmailVerificationToken;
import kaiquebt.dev.instrutorbrasil.model.User;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Modifying;
import org.springframework.data.jpa.repository.Query;
import org.springframework.stereotype.Repository;

import java.time.Instant;
import java.util.Optional;

@Repository
public interface EmailVerificationTokenRepository extends JpaRepository<EmailVerificationToken, Long> {

	Optional<EmailVerificationToken> findByToken(String token);

	void deleteByUser(User user);

	@Modifying
	@Query("DELETE FROM EmailVerificationToken evt WHERE evt.expiryDate < :date")
	void deleteByExpiryDateBefore(Instant date);
}
